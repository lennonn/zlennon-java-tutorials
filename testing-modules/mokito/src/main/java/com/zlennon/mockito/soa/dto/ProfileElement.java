package com.zlennon.mockito.soa.dto;

public class ProfileElement {

	private String geneticMarker;
	private String alleleA;
	private String alleleB;
	
	public String getGeneticMarker() {
		return geneticMarker;
	}
	public void setGeneticMarker(String geneticMarker) {
		this.geneticMarker = geneticMarker;
	}
	public String getAlleleA() {
		return alleleA;
	}
	public void setAlleleA(String alleleA) {
		this.alleleA = alleleA;
	}
	public String getAlleleB() {
		return alleleB;
	}
	public void setAlleleB(String alleleB) {
		this.alleleB = alleleB;
	}

	
}
